.global lfb_game_over
lfb_game_over:
   ldr x0, =game_over_img
   ldrb w1, [x0]  // Get first pixel
   ldr x2, lfb

   // Accumalator to loop though lfb
   mov x3, #0
   ldr x4, =0x4b000
draw_game_over_loop:
   ldrb w1, [x0, x3]

   // load blue component
   and w5, w1, #0b11
   lsl w5, w5, #6

   // load green component
   and w6, w1, #0b11100
   lsl w6, w6, #11
   orr w5, w5, w6

   // load red component
   and w7, w1, #0b11100000
   lsl w7, w7, #16
   orr w5, w5, w7

   str w5, [x2, x3, lsl #2]

   // Add to accum
   add x3, x3, #1
   cmp x3, x4
   blt draw_game_over_loop

   ret
