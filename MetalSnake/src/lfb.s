.global lfb_drawsnake
lfb_drawsnake:

    sub sp, sp, 16
    str x29, [sp, #8]
    str x30, [sp]


    // x0 = backbuffer
    ldr x0, =backbuffer


    // === clear the screen ===
    mov x1, #0
    ldr x2, =0x4b000
    mov w3, #0
_lfb_drawsnake_clear_loop:
    strb w3, [x0, x1]

    add x1, x1, #1
    cmp x1, x2
    blt _lfb_drawsnake_clear_loop
    // === /clear the screen ===



    ldr w11, =0xAA // the snake and food are drawn in red
    // === draw the food ===

    ldr x1, food_idx
    ldr x2, =food_array

    // x3 = y, x4 = x
    ldr x3, [x2, x1, lsl #3]
    mov w4, w3
    lsr x3, x3, #32

    // Get pixel coordinates on a
    // 16*16 grid
    lsl w3, w3, #4
    lsl w4, w4, #4

    // y, x iterators
    mov w5, w3
    mov w6, w4

    // loop terminating conditions
    add w7, w3, #16
    add w8, w4, #16

_lfb_drawfood_loop_y:
    mov w6, w4

_lfb_drawfood_inner_loop_x:

    // pixel index = (y * 640) + x
    mov w9, #640
    mul w9, w5, w9
    // mul w9, w5, w9
    add w9, w9, w6

    // write to buffer
    strb w11, [x0, x9]

    add w6, w6, #1
    cmp w6, w8
    blt _lfb_drawfood_inner_loop_x

    add w5, w5, #1
    cmp w5, w7
    blt _lfb_drawfood_loop_y

    // === /draw the food ===



    // === draw the snake ===

    // we start by looping through snake body
    // and retrieving the coordinates of each segment
    mov x1, #0
    ldr x2, snake_len
    ldr x3, =snake_body
    ldr w11, =0xFF // the snake is drawn in white
_lfb_drawsnake_loop:

    // x4 = y, x5 = x
    ldr x5, [x3, x1, lsl #3]
    lsr x4, x5, #32
    lsl x5, x5, #32
    lsr x5, x5, #32

    // multiply by 16 to get pixel coordinates
    // since snake coordinates are expressed
    // on a grid where each square is 16*16 pixels
    lsl w4, w4, #4
    lsl w5, w5, #4

    // w6 and w7 are used to loop through pixels
    // and actually write to the buffer
    mov w6, w4
    mov w7, w5

    // w8 and w9 are the lower-right corner of the
    // square, where we stop drawing
    add w8, w6, #16
    add w9, w7, #16

_lfb_drawsnake_inner_loop_y:

    // reset pixel x-coordinate to 0
    mov w7, w5

_lfb_drawsnake_inner_loop_x:

    // pixel index = (y * 640) + x
    mov w10, #640
    mul w10, w6, w10
    add w10, w10, w7

    // write to buffer
    strb w11, [x0, x10]

    add w7, w7, #1
    cmp w7, w9
    blt _lfb_drawsnake_inner_loop_x

    add w6, w6, #1
    cmp w6, w8
    blt _lfb_drawsnake_inner_loop_y

    add x1, x1, #1
    cmp x1, x2
    blt _lfb_drawsnake_loop

    // === /draw the snake ===


_lfb_drawsnake_swap_buffers:

    // swap back and front buffers since
    // writing directly to the front buffer
    // causes flickering and looks bad

    // 'swap' is a misleading word since nothing
    // is being swapped, effectively we are doing
    // memcpy(lfb, backbuffer)

    ldr x1, lfb

    mov x2, #0
    ldr x3, =0x4b000
_lfb_swap_loop:
    ldrb w4, [x0, x2]

    cbz w4, _lfb_swap_blue
    cmp w4, 0xAA
    beq _lfb_swap_red
    ldr w4, =0xFFFFFFFF
    b _lfb_swap_store
_lfb_swap_blue:
    mov w4, #0x000000FF
    b _lfb_swap_store
_lfb_swap_red:
    mov w4, #0x00FF0000
_lfb_swap_store:
    str w4, [x1, x2, lsl #2]

    add x2, x2, #1
    cmp x2, x3
    blt _lfb_swap_loop
    ldr x29, [sp, #8]
    ldr x30, [sp]
    add sp, sp, 16

    ret


